public class Amigos {
    private String nome;
    private String numero_conta;


    public void setNome(String nome){
        this.nome=nome;
    }
    public void setConta(String numero_conta){
        this.numero_conta=numero_conta;
    }
    public String getNome(){
        return this.nome;
    }
    public String getNumero(){
        return this.numero_conta;
    }
}
